#include <iostream>
using namespace std;

int main(){
    /*
    
    Comparison
    == 
    !=
    > < 
    >= 
    <=

    */
    int num1 = 1;
    int num2 = 0;
    cout <<"Is num1 is equal num2 : " << (num1 == num2) << endl;
    cout << "Is num1 is not equal num2 : " <<(num1 != num2) << endl;
    cout << "Is num1 greater than num2 : " << (num1 > num2) << endl;
    cout << "Is num1 less than num2 : " << (num1 < num2) << endl;
    cout << "Is num1 greater than or equal num2 : " << (num1 >= num2) << endl;
    cout << "Is num1 less than or equal num2 : " << (num1 <= num2) << endl;

    return 0;
}